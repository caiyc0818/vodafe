package com.huawei.vodafone.bean;

public class ParameterValue {

	private String parameterInstId;

	private String parameterId;

	private String parameterCode;

	private String parameterValue;

	private String parameterActionType;

	public String getParameterInstId() {
		return parameterInstId;
	}

	public void setParameterInstId(String parameterInstId) {
		this.parameterInstId = parameterInstId;
	}

	public String getParameterId() {
		return parameterId;
	}

	public void setParameterId(String parameterId) {
		this.parameterId = parameterId;
	}

	public String getParameterCode() {
		return parameterCode;
	}

	public void setParameterCode(String parameterCode) {
		this.parameterCode = parameterCode;
	}

	public String getParameterValue() {
		return parameterValue;
	}

	public void setParameterValue(String parameterValue) {
		this.parameterValue = parameterValue;
	}

	public String getParameterActionType() {
		return parameterActionType;
	}

	public void setParameterActionType(String parameterActionType) {
		this.parameterActionType = parameterActionType;
	}

}